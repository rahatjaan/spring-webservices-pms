package com.spring.pms.dto;

public class GuestInfo {
	private int id;
	private String firstName;
	private String lastName;
	private String fullAddress;
	private String mobileNumber;
	private String ratePlan;
	private String hhNumber;
	private String al;
	private String bonusAl;
	private String confirmationNumber;
	private String memberShipNumber;
	private String bonusCode;
	private String groupName;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public void setLastName(String lastName) {
		this.lastName = lastName;
	}
	public String getFullAddress() {
		return fullAddress;
	}
	public void setFullAddress(String fullAddress) {
		this.fullAddress = fullAddress;
	}
	public String getMobileNumber() {
		return mobileNumber;
	}
	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}
	public String getRatePlan() {
		return ratePlan;
	}
	public void setRatePlan(String ratePlan) {
		this.ratePlan = ratePlan;
	}
	public String getHhNumber() {
		return hhNumber;
	}
	public void setHhNumber(String hhNumber) {
		this.hhNumber = hhNumber;
	}
	public String getAl() {
		return al;
	}
	public void setAl(String al) {
		this.al = al;
	}
	public String getBonusAl() {
		return bonusAl;
	}
	public void setBonusAl(String bonusAl) {
		this.bonusAl = bonusAl;
	}
	public String getConfirmationNumber() {
		return confirmationNumber;
	}
	public void setConfirmationNumber(String confirmationNumber) {
		this.confirmationNumber = confirmationNumber;
	}
	public String getMemberShipNumber() {
		return memberShipNumber;
	}
	public void setMemberShipNumber(String memberShipNumber) {
		this.memberShipNumber = memberShipNumber;
	}
	public String getBonusCode() {
		return bonusCode;
	}
	public void setBonusCode(String bonusCode) {
		this.bonusCode = bonusCode;
	}
	public String getGroupName() {
		return groupName;
	}
	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}	
}
